age = input('What is your current age? ')
plusone = int(age) + 1
print(f'Your age after your next birthday is {plusone}.')

cartons = input('How many egg cartons do you have? ')
eggs = int(cartons) * 12
print(f'You have {eggs} eggs.')

cookies = input ('How many cookies do you have? ')
people = input ('How many people are there? ')
cookie_ration = int(cookies) / int(people)
print(f'Your cookie ration is {cookie_ration:.5}.')